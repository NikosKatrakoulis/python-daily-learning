# Favorite Fruit: Make a list of your favorite fruits and then write a
# series of independent if statements that check for certain fruits in
# your list. Make a list of your three favorite fruits and call it
# favorite_fruits. Write five if statements. Each should check whether a
# certain kind of fruit is in your list. If the fruit is in your list,
# the if block should print a statement, such as You really like bananas!

favorite_fruits = ['watermelon', 'banana', 'grape']

if 'watermelon' in favorite_fruits:
    print("I really love watermelons!")

if 'cherish' in favorite_fruits:
    print("I really love cherishes.")

if 'banana' in favorite_fruits:
    print("I really love bananas!")

if 'apple' in favorite_fruits:
    print("I really love apples.")

if 'grape' in favorite_fruits:
    print("I really love grapes!")
